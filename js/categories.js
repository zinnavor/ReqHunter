/**
 * Определяет категорию товара по названию.
 * Возвращает объект категории или null если категория не определена.
 */
function detectCategory(query) {
  if (!query || typeof query !== 'string') return null;

  const normalizedQuery = query.toLowerCase().trim();
  const queryWords = normalizedQuery.split(/\s+/);

  let bestMatch = null;
  let bestScore = 0;

  for (const [categoryId, category] of Object.entries(CATEGORIES)) {
    let score = 0;

    for (const keyword of category.keywords) {
      // Точное совпадение с запросом
      if (normalizedQuery === keyword) {
        score += 100;
        continue;
      }

      // Запрос содержит ключевое слово
      if (normalizedQuery.includes(keyword)) {
        // Более длинные ключевые слова получают больший вес
        score += 10 + keyword.length;
        continue;
      }

      // Ключевое слово содержит запрос (частичное совпадение)
      if (keyword.includes(normalizedQuery) && normalizedQuery.length >= 3) {
        score += 5;
        continue;
      }

      // Проверяем каждое слово запроса
      for (const word of queryWords) {
        if (word.length < 3) continue;
        if (keyword.includes(word) || word.includes(keyword)) {
          score += 3;
        }
      }
    }

    if (score > bestScore) {
      bestScore = score;
      bestMatch = category;
    }
  }

  // Минимальный порог уверенности
  return bestScore >= 3 ? bestMatch : null;
}

/**
 * Возвращает список всех категорий для выпадающего списка.
 */
function getAllCategories() {
  return Object.values(CATEGORIES);
}

/**
 * Возвращает категорию по ID.
 */
function getCategoryById(id) {
  return CATEGORIES[id] || null;
}
